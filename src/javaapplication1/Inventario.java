    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    // importamos jtable para guardar los tados
        File file = new File("inventariosave.txt");
        try {
            FileWriter fw = new FileWriter(file);
             BufferedWriter bw = new BufferedWriter(fw);
              for(int i = 0; i < jTable2.getRowCount(); i++){//rows
                  for(int j = 0; j < jTable2.getColumnCount(); j++){//columns
                    bw.write(jTable2.getValueAt(i, j).toString()+",");
                }
                  bw.newLine();
              }
               bw.close();
            fw.close();
        JOptionPane.showMessageDialog(null,"archivo guardado!");
        } catch (IOException ex) {
            Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showInternalMessageDialog(null,"Error no se pudo crear el archivo");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
          File file = new File("inventariosave.txt");
        
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
            DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
            Object[] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split(",");
                model.addRow(row);
            }
            
        } catch (FileNotFoundException ex) {
            
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

