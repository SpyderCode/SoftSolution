    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
        //borrar linea
        if(jTable2.getSelectedRowCount()==1){
            // si solo hay una linea seleccionadad entonces la borra
            tableModel.removeRow(jTable2.getSelectedRow());
        }else{
            if(jTable2.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "No hay datos en la tabla", " ERROR ",JOptionPane.ERROR_MESSAGE);
            
            }else{
                //si hay datos en la tabla pero no se han selecionado
                JOptionPane.showMessageDialog(this, "seleccione un renglon por favor", " ERROR ",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_borrarActionPerformed

