    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     try{
        // obtenemos los numeor
        String parte= jComboBox1.getSelectedItem().toString();
        String tipo;
        double resultado;
        double numcant=Double.parseDouble(numcan.getText());
        double numprec=Double.parseDouble(numpre.getText());
        // obtenemos el tipo de uniforme
       int i=unifo.getSelectedIndex();
       if(i==0){
           resultado=numcant*numprec;
           tipo=("Uniforme Escolar Completo(s) costo ");   
       }else if(i==1){
           resultado=numcant*numprec;
           tipo=("Uniforme Medico Completo(s) costo ");   
       }else {
       resultado=numcant*numprec;
       tipo=("Pieza: "+parte); 
       }
       //imprimimos resultado
       txtResultado.setText(tipo+"=  $"+String.valueOf(resultado));
     }catch(Exception e){
         JOptionPane.showMessageDialog(this, "Error Asegurse de que los datos sean solo numeros", " ERROR ",JOptionPane.ERROR_MESSAGE);
     }
    }//GEN-LAST:event_jButton1ActionPerformed

